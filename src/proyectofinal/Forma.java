    private void jMIAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMIAbrirActionPerformed
        // TODO add your handling code here:
        ClaseArchivoBinarioEntero archivo = new ClaseArchivoBinarioEntero();
        Numero datos = new Numero();
        
        selArch = new JFileChooser();
        int valor = selArch.showOpenDialog(this);
        if(valor == JFileChooser.APPROVE_OPTION)
        {
            String nomArchivo = selArch.getSelectedFile().toString();
            archivo.leerVecEnArchivo(nomArchivo, datos, modelo);
        }
        jSpinPosicion.setValue(datos.num);
        jTextCantidad.setText(datos.num+"");
        jTextCanPro.setText(datos.num+"");
    }//GEN-LAST:event_jMIAbrirActionPerformed

