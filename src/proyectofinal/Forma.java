    public void Mostrar()
    {
        double total = 0;
        int can = 0; 
        DecimalFormat f = new DecimalFormat("#,###.00");
        
        for(int r=0; r<jTable1.getRowCount(); r++)
        {
            
            jTable1.setValueAt(lista[r].getProducto(), r, 0);
            jTable1.setValueAt(lista[r].getCodigo(), r, 1);
            jTable1.setValueAt(f.format(lista[r].getPrecio()), r, 2);
            jTable1.setValueAt(lista[r].getCantidad(), r, 3);
            jTable1.setValueAt(f.format(lista[r].Total()), r, 4);
                       
            total += lista[r].Total();
            can += lista[r].getCantidad();
        }
        
        jTextTotal.setText(f.format(total));
        jTextCanPro.setText(can+"");
    }
    
